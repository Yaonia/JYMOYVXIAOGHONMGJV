#程序功能：输入语文成绩，判断其对应等级。如果大于等于125，则输出“优秀”，
#否则如果大于等于90，则输出“及格”，否则输出“不及格”。
#按照程序功能要求，修改完善程序，在<1>、<2>、<3>处填入正确代码，不要修改其它代码
cj=float(input("请输入语文成绩："))
if cj >= 125:    #删除语句中的"<1>"，并在此处填入正确代码
    print("优秀")
elif cj >= 90:  #删除语句中的"<2>"，并在此处填入正确代码
    print("及格")
else:
    print("不及格")  #删除语句中的"<3>"，并在此处填入正确代码
