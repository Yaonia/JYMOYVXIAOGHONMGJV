#输入物理成绩，判断其对应等级。如果大于等于90，则输出“优秀”，
#否则如果大于等于80，则输出“良好”，否则如果大于等于60，则输出“合格”，否则输出“不合格”。
wl=int(input("请输入物理成绩:"))
if wl >= 90:   #删除语句中的"<1>"，并在此处填入正确代码
    print("优秀")
elif wl >= 80: 
    print("良好") 
elif wl >= 60:   #删除语句中的"<2>"，并在此处填入正确代码
    print("合格")
else:     #删除语句中的"<3>"，并在此处填入正确代码
    print("不合格")
