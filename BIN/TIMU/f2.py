#程序功能：输入比赛名次，判断其对应等级。如果小于等于10，则输出“一等奖”,
#否则如果小于等于30，则输出“二等奖”,否则输出“三等奖”
#按照程序功能要求，修改完善程序，在<1>、<2>、<3>处填入正确代码，不要修改其它代码
mc=int(input("请输入比赛名次："))
if mc <= 10:           #删除语句中的"<1>"，并在此处填入正确代码
     print("一等奖")
elif mc <= 30:         #删除语句中的"<2>"，并在此处填入正确代码
     print("二等奖")
else:
     print("三等奖")    #删除语句中的"<3>"，并在此处填入正确代码

